package Construtor;
public class ExConstrutorCarros {
    public double velMaxima;
    public String modelo;
    public String ano;
    public String cor;
    public double chassis;

    public ExConstrutorCarros( double velMaxima , String modelo, String ano, String cor, double chassis){
        this.velMaxima = velMaxima;
        this.modelo = modelo;
        this.ano = ano;
        this.cor = cor;
        this.chassis = chassis;
    }
}
